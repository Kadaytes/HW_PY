year = int( input("введите год в формате ГГГГ:  "))
answer = ' '
if year <= GRIGORIAN :
    answer = 'не верный год'
elif year % 4 != 0 or year % 100 == 0 and year % 400 != 0 :
    answer = "обычный"
else:
    answer = "высокостный"
print(answer)

