# 3. Напишите код, который запрашивает число и сообщает является ли оно простым или составным.
#  Используйте правило для проверки: “Число является простым, 
#  если делится нацело только на единицу и на себя”. 
#  Сделайте ограничение на ввод отрицательных чисел и чисел больше 100 тысяч.

number = int(input("введите число от 1 до 100 000: "))
if number >=1  and number <= 100000 :
    if number == 1 :
        print(number, "число простое") 
    elif number > 1 and all(number % i for i in range(2, int(number**0.5) + 1)):
        print(number, "число простое")
    else:
        print(number, "число составное")
else:
    print(number, "не правильный ввод числа")